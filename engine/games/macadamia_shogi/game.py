"""Macadamia Shogi -- H. G. Muller's factor-two shrink of the historic
Maka Dai Dai Shogi: a 13x13 large-shogi variant, 48 pieces a side of 26 types
(11 more via promotion), DROP-LESS, won by capturing ALL enemy royals.

Authoritative source (implementation-grade, followed exactly, overrides all
briefs): https://www.chessvariants.com/rules/macadamia-shogi  (H. G. Muller,
2015).  Its interactive-diagram config gives the exact per-piece Betza moves,
the exact initial squares and the promoted forms; the Rules prose defines
PROMOTION BY CAPTURE, the Lion / Wolf(Lion Dog) / Emperor mechanics and the win
condition.  The variant is directly played by HaChu 0.21 (``variant
macadamia-shogi``), used as a differential oracle for setup + movegen (see
selftest.py / the dev harness).

Modelled on games/nutty_shogi and games/chu_shogi (drop-less, no-check,
royal-capture ShogiLike subclasses with a full Lion engine).  Key facts:

* **Board 13x13**, files a-m (col 0-12), ranks 1-13 (row 0-12).  Player 0 =
  Sente (Black) at the bottom, advancing +row.  DROP-LESS.
* **PROMOTION BY CAPTURE (the crux).**  There is NO promotion zone.  A piece may
  promote only at the end of a turn in which it CAPTURED something.  Each type
  has ONE fixed promoted form.  Promotion is OPTIONAL, EXCEPT it is MANDATORY
  when the captured piece was itself already promoted.  Special override: when a
  non-royal piece captures a Priest/Saint it becomes a **Saint**, and when it
  captures a Pirate/Ghost it becomes a **Ghost** -- forced, even if it was
  already promoted (so the Saint & Ghost virtually never leave play).  Royals
  (King, Elephant and their promotions) never become Saint/Ghost; Queen & Castle
  never promote at all.
* **Win as event.**  Royal pieces are the King, the Emperor (promoted King) and
  the Prince (promoted Drunk Elephant -- a second King).  The game ends the
  instant a side has NO royal left.  Movegen is pseudo-legal.
* **Repetition / draws:** fourfold repetition of (position + side to move) or a
  hard ply cap -> an honest draw.  CVP's intent-based perpetual-check /
  perpetual-chase loss rules are a documented simplification (see rules.md).

Internal letters == the CVP interactive-diagram ids:
  P pawn, I snake, C copper, S silver, G gold, T tiger, X phoenix, O kirin,
  N lion, Y pirate, Z priest, E elephant, K king, L lance, F dragon,
  A guardian, M wrestler, U left-chariot, V right-chariot, B bishop, R rook,
  J capricorner, H hook-mover, W wolf, D castle, Q queen.
A promoted piece keeps its base letter with the square flagged in ``promoted``;
its moves come from PROMO_MOVE[letter] (the Saint/Ghost capture-override instead
changes the letter to Z/Y with the promoted flag set).
"""

